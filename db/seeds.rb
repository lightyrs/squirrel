sources.each do |source_hash|
  source_hash.each do |key, val|
    puts "Creating Source: #{key}".blue
    Source.find_or_create_by(name: key) do |source|
      source.url = val
    end
  end
end

content_types.each do |content_type|
  puts "Creating ContentType: #{content_type}".green
  ContentType.find_or_create_by(name: content_type)
end

classifications.each do |key, val|
  val.each do |name|
    puts "Creating Classification: [#{key}] #{name}".yellow
    Classification.find_or_create_by(
      name: name,
      content_type_id: ContentType.find_by(name: key).id
    )
  end
end
